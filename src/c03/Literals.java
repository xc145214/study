package c03;

public class Literals {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		//16进制
		char c = 0xffff;
		byte b = 0x7f;
		short s = 0x7fff;
		int i1 = 0x2f;
		int i2 = 0X2F;
		int i3 = 0177;
		
		//hex and oct also work with long
		long n1 = 200L;
		long n2 = 200l;
		long n3 = 200;
		
		float f1 = 1;
		float f2 = 1F;
		float f3 = 1f;
		float f4 = 1e-45f;
		float f5 = 1e+9f;
		
		double d1= 1D;
		double d2 = 1d;
		double d3 = 47e47d;
		
				
	}

}
